# int() converte o valor em um número inteiro
# float() converte o valor em um número decimal
# bool() converte o valor em um booleano
# str() converte o valor em uma string

# Calculadora de soma simples
primeiro = float(input("Digite o primeiro número: "))
segundo = float(input("Digite o segundo número: "))
soma = primeiro + segundo

print("Resultado: " + str(soma))
 